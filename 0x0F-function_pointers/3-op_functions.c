#include "3-calc.h"


/**
 * op_add - additions
 * @a: first int
 * @b: second int
 *
 * Return: sum
 *
 */
int op_add(int a, int b)
{
	return (a + b);
}


/**
 * op_sub - difference
 * @a: first int
 * @b: second int
 *
 * Return: differenec
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiplication
 * @a: first int
 * @b: second int
 *
 * Return: product
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - division
 * @a: numenator
 * @b: denominator
 *
 * Return: division
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - remainder calculator
 * @a: first int
 * @b: second int
 *
 * Return: remainder
 */
int op_mod(int a, int b)
{
	return (a % b);
}
